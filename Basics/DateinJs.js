
let myDate= new Date();

console.log(myDate)

console.log(myDate.toString());//Coordinated Universal Time shows Time

console.log(myDate.toDateString());//Shows only day and Date

console.log(myDate.toISOString());//Some Entirely different Format with z

console.log(myDate.toLocaleString()) //6/7/2024, 4:37:17 PM

console.log(myDate.toTimeString());//16:38:08 GMT+0000 (Coordinated Universal Time)

